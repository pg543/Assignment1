public class AdditionProj {
	
	public static void main(String[] args) {
		
		int firstNumber = 23343434;
		short secondNumber = 34;
		short thirdNumber = 25;
				
		short addedValue = (short) (firstNumber + secondNumber);
		int finalValue = addedValue - thirdNumber;
		
		System.out.println("Final Value from three numbers is " + finalValue);
	}
	

}
